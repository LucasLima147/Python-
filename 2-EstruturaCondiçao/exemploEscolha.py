# Exemplo de IF, ELSE e ELif(SE, SENÂO e SENÂO SE )
op = int(input("escolha entre as opções: \n 1-Administração \n 2 Sistemas de Informação \n 3-Pedagogia \n 4-Eng. Produção \n 5-Contabilidade \n Escolha: "))
if op == 1:
    print("Administração")
elif op == 2:
    print("Sistemas de Informação")
elif op == 3:
    print("Pedagogia")
elif op == 4:
    print("Eng. de Produção")
elif op == 5:
    print("Contabilidade")
else:
    print("opcao invalida")